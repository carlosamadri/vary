// Calcular el perimetro de un circulo
function perimetroCirculo(d){
    return `El perimetro del circulo es: ${d*Math.PI}`;
}

const perimetroCirculoFlecha = (d) => "El perimetro del circulo es: " + d*Math.PI;

// Calcular el area de un rectangulo
function areaRectangulo(a, b){
    return `El área del rectangulo es: ${a*b}`;
}

const areaRectanguloFlecha = (a, b) => "El área del rectagulo es: " + a*b;

// Calcular el cuadrado de un numero
