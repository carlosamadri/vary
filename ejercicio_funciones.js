// Calcular la conversion de grados celsius a farenheit
function celsiusAFarenheit(c){
    return `El equivalente en farenheit es: ${(c*1.8)+32}°F`;
}

const celsiusAFarenheitFlecha = (c) => "El equivalente en farenheit es:" + (c*1.8)+32;

